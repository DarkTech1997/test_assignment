#!/bin/bash

# test_runner.sh – скрипт для тестирования mask_finder.py
# Запуск: chmod +x test_runner.sh && ./test_runner.sh

# Имя файла с программой (должен лежать в той же папке)
PYTHON_CMD="python"
SCRIPT="mask_finder.py"

# Проверяем, существует ли файл программы
if [ ! -f "$SCRIPT" ]; then
    echo "Ошибка: $SCRIPT не найден! Поместите скрипт в одну папку с программой."
    exit 1
fi

# Создаём имя лог-файла с текущей датой и временем
LOG_FILE="test_log_$(date +%d_%m_%Y).txt"

# Записываем заголовок лога
echo "=====================================================" > "$LOG_FILE"
echo "   Лог тестирования программы mask_finder.py" >> "$LOG_FILE"
echo "   Дата и время запуска: $(date '+%Y-%m-%d %H:%M:%S')" >> "$LOG_FILE"
echo "=====================================================" >> "$LOG_FILE"
echo "" >> "$LOG_FILE"

# Глобальный счётчик тестов
TEST_NUM=0

# Функция для вывода заголовка группы в лог
print_group_header() {
    local group_name="$1"
    echo "" >> "$LOG_FILE"
    echo ">>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>" >> "$LOG_FILE"
    echo ">>>  $group_name" >> "$LOG_FILE"
    echo ">>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>" >> "$LOG_FILE"
    echo "" >> "$LOG_FILE"
}

# Функция для запуска одного теста
# Параметры: описание, аргументы, ожидаемый код возврата
run_test() {
    local description="$1"
    local args="$2"
    local expected="$3"

    # Увеличиваем счётчик
    ((TEST_NUM++))

    echo "-----------------------------------------------------" >> "$LOG_FILE"
    echo "Тест #$TEST_NUM: $description" >> "$LOG_FILE"
    echo "Команда: $PYTHON_CMD $SCRIPT $args" >> "$LOG_FILE"
    echo "Вывод программы:" >> "$LOG_FILE"

    # Запускаем программу, сохраняя stdout и stderr
    output=$($PYTHON_CMD $SCRIPT $args 2>&1)
    exit_code=$?

    echo "$output" >> "$LOG_FILE"
    echo "Ожидаемый код возврата: $expected" >> "$LOG_FILE"
    echo "Фактический код возврата: $exit_code" >> "$LOG_FILE"

    # Пометка о совпадении/несовпадении
    if [ "$exit_code" -eq "$expected" ]; then
        echo ">>> РЕЗУЛЬТАТ: СООТВЕТСТВУЕТ" >> "$LOG_FILE"
    else
        echo ">>> РЕЗУЛЬТАТ: НЕ СООТВЕТСТВУЕТ (ожидался $expected)" >> "$LOG_FILE"
    fi
    echo "" >> "$LOG_FILE"
}

# ========== ГРУППА 1: Нормальные IPv4 (ожидаемый код 0) ==========
print_group_header "ГРУППА 1: Нормальные IPv4 пары"
run_test "IPv4: два одинаковых адреса" "192.168.1.1 192.168.1.1" 0
run_test "IPv4: соседние адреса (разница в последнем бите)" "10.0.0.1 10.0.0.0" 0
run_test "IPv4: классический пример /22" "192.168.1.1 192.168.2.1" 0
run_test "IPv4: разные классы, маска /0" "1.1.1.1 200.200.200.200" 0
run_test "IPv4: граничные значения (0 и 255)" "0.0.0.0 255.255.255.255" 0
run_test "IPv4: внутри одной подсети /24" "192.168.1.10 192.168.1.200" 0
run_test "IPv4: адреса из диапазона localhost" "127.0.0.1 127.0.0.255" 0

# ========== ГРУППА 2: Нормальные IPv6 (ожидаемый код 0) ==========
print_group_header "ГРУППА 2: Нормальные IPv6 пары"
run_test "IPv6: полные адреса (одинаковые)" "2001:0db8:85a3:0000:0000:8a2e:0370:7334 2001:0db8:85a3:0000:0000:8a2e:0370:7334" 0
run_test "IPv6: сжатая форма ::1" "::1 ::2" 0
run_test "IPv6: разница в середине" "2001:db8::1 2001:db9::1" 0
run_test "IPv6: сжатие в начале и конце" "::1 ::2" 0
run_test "IPv6: максимальная маска /128" "fe80::1 fe80::1" 0
run_test "IPv6: нулевой адрес и другой" ":: ::1" 0

# ========== ГРУППА 3: Смешанные версии (ожидаемый код 2) ==========
print_group_header "ГРУППА 3: Смешанные версии (должна быть ошибка)"
run_test "Смешанные версии: IPv4 + IPv6" "192.168.1.1 2001:db8::1" 2
run_test "Смешанные версии: IPv6 + IPv4" "::1 8.8.8.8" 2

# ========== ГРУППА 4: Неверные форматы IPv4 (ожидаемый код 2) ==========
print_group_header "ГРУППА 4: Неверные форматы IPv4"
run_test "Неверный IPv4: число >255" "256.1.1.1 192.168.1.1" 2
run_test "Неверный IPv4: буквы" "abc.def.ghi.jkl 10.0.0.1" 2
run_test "Неверный IPv4: 5 октетов" "1.2.3.4.5 192.168.1.1" 2
# Ведущий ноль – в текущей программе не считается ошибкой, поэтому ожидаем 0
run_test "Неверный IPv4: ведущий ноль (опционально)" "01.2.3.4 10.0.0.1" 0

# ========== ГРУППА 5: Неверные форматы IPv6 (ожидаемый код 2) ==========
print_group_header "ГРУППА 5: Неверные форматы IPv6"
run_test "Неверный IPv6: слишком много групп" "2001:db8:1:2:3:4:5:6:7 2001:db8::1" 2
run_test "Неверный IPv6: недопустимые символы" "2001:db8::g 2001:db8::1" 2
run_test "Неверный IPv6: двойное ::" "2001::db8::1 2001::db8::2" 2
run_test "Неверный IPv6: недостаточно групп (7 вместо 8)" "2001:db8:1:2:3:4:5 2001:db8::1" 2
run_test "Неверный IPv6: два сжатия (:: и ::)" "2001:db8::1::2 2001:db8::1" 2
run_test "Неверный IPv6: группа длиной 5 символов" "2001:db8:12345::1 2001:db8::1" 2
run_test "Неверный IPv6: тройное двоеточие (:::)" "2001:::db8::1 2001:db8::1" 2

# ========== ГРУППА 6: Ошибки количества аргументов (ожидаемый код 1) ==========
print_group_header "ГРУППА 6: Неверное количество аргументов"
run_test "Нет аргументов" "" 1
run_test "Один аргумент" "192.168.1.1" 1
run_test "Три аргумента (лишний)" "192.168.1.1 192.168.2.1 10.0.0.1" 1

# ========== ГРУППА 7: Пограничные случаи (ожидаемый код 0) ==========
print_group_header "ГРУППА 7: Пограничные случаи (нестандартные маски)"
run_test "IPv4: маска /31 (адреса различаются только в последнем бите)" "10.0.0.0 10.0.0.1" 0
run_test "IPv4: маска /1 (различаются во втором бите)" "128.0.0.1 64.0.0.1" 0
run_test "IPv6: маска /64 (стандартная префиксная граница)" "2001:db8:1234:5678::1 2001:db8:1234:5679::1" 0

# ========== Завершение ==========
echo "=====================================================" >> "$LOG_FILE"
echo "Тестирование завершено. Лог сохранён в файл: $LOG_FILE" >> "$LOG_FILE"
echo "=====================================================" >> "$LOG_FILE"

# Вывод сообщения в консоль
echo "Тестирование завершено. Результаты записаны в $LOG_FILE"